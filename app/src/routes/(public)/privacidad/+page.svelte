	<h3>6.1 Eliminación automática</h3>
	<ul>
		<li>
			Las evidencias fiscales pueden eliminarse de inmediato tras el cierre aprobado por los colaboradores,
			cuando previamente se hubiere manifestado la legitimidad conforme a los flujos de la plataforma.
		</li>
		<li>Los metadatos de auditoría pueden conservarse hasta 6 meses adicionales para fines de registro.</li>
		<li>
			La documentación de verificación puede expirar automáticamente y requerir una nueva verificación
			periódica.
		</li>
	</ul>

	<h3>6.2 Eliminación manual</h3>
	<ul>
		<li>
			Las instituciones pueden solicitar la eliminación de evidencias no fiscales desde su panel cuando las
			funcionalidades lo permitan.
		</li>
		<li>
			Un administrador puede ordenar eliminación anticipada si detecta contenido que infrinja los términos
			de uso.
		</li>
	</ul>

	<h3>6.3 Excepciones</h3>
	<p>La retención puede extenderse cuando exista:</p>
	<ul>
		<li>Proceso judicial o reclamo en curso.</li>
		<li>Requerimiento de autoridad competente.</li>
		<li>Obligación fiscal u otra normativa que imponga conservar documentos por más tiempo.</li>
	</ul>

	<h2>7. Seguridad y Ley N.º 26.388</h2>
	<p>
		Se aplican medidas técnicas y organizativas razonables: HTTPS, controles de acceso, monitoreo ante
		sospechas de fraude, logs asociados a cuentas cuando corresponda, y uso de infraestructura con buenas
		prácticas de ciberseguridad. Existe un mecanismo accesible para reportar irregularidades.
	</p>

	<h2>8. Tratamiento de datos en donaciones y proyectos</h2>
	<p>
		Los datos vinculados a proyectos, donaciones y evidencias se tratan para permitir el cumplimiento de
		los fines declarados por colaboradores e instituciones (incluidos “cargos” o destinos específicos), la
		rendición de cuentas y el cierre documentado de proyectos, en coherencia con el marco civil y las
		políticas de transparencia del proyecto (véase también los
		<a href="/terminos">términos y condiciones</a>).
	</p>

	<h2>9. Cambios y contacto</h2>
	<p>
		Esta política puede actualizarse. Los cambios relevantes se comunicarán por los medios razonables
		disponibles. Para ejercer derechos o consultas sobre privacidad, utilizá los canales de contacto del sitio.
	</p>
