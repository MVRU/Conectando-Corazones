	<h1>Política de privacidad</h1>
	<p class="lead text-slate-600">
		<strong>Versión 1.0.</strong> Esta política describe cómo Conectando Corazones trata los datos
		personales conforme a la <strong>Ley N.º 25.326</strong> de Protección de los Datos Personales y normas
		complementarias. Al registrarte aceptás esta política en la versión vigente.
	</p>

	<h2>1. Responsable y finalidad</h2>
	<p>
		El responsable del tratamiento de los datos personales es quien opere la plataforma Conectando Corazones
		según se identifique en el sitio y en los registros ante la autoridad de aplicación. Los datos se
		recogen para:
	</p>
	<ul>
		<li>Gestionar el registro de instituciones y colaboradores y la seguridad de la cuenta.</li>
		<li>Permitir la publicación y seguimiento de proyectos y colaboraciones.</li>
		<li>Facilitar la comunicación (por ejemplo, mensajería) y la trazabilidad de donaciones y evidencias.</li>
		<li>Cumplir obligaciones legales y responder a requerimientos legítimos de autoridades.</li>
		<li>Mejorar el servicio y la seguridad, mediante logs y auditoría en la medida necesaria.</li>
	</ul>
	<p>
		La información debe ser <strong>precisa, pertinente y no excesiva</strong> respecto de las finalidades
		declaradas, y utilizarse exclusivamente para esos fines salvo consentimiento u obligación legal.
	</p>

	<h2>2. Información al titular (Ley 25.326)</h2>
	<p>
		Se informa de modo claro y accesible: <strong>qué datos</strong> se recopilan, <strong>para qué</strong>,
		<strong>quién</strong> es responsable del archivo o tratamiento, <strong>quiénes</strong> pueden ser
		destinatarios y cómo podés ejercer los derechos de acceso, rectificación, actualización, supresión cuando
		corresponda, y demás derechos previstos por la ley.
	</p>

	<h2>3. Datos sensibles</h2>
	<p>
		Conforme al art. 2 de la Ley N.º 25.326, no se solicitará ni se fomentará el ingreso de
		<strong>datos sensibles</strong> (origen racial o étnico, opiniones políticas, convicciones religiosas,
		filosóficas o morales, afiliación sindical, datos relativos a la salud o la vida sexual, datos
		genéticos o biométricos, etc.) salvo supuestos expresamente autorizados por ley. Los perfiles, chats y
		formularios están diseñados para minimizar la recolección de este tipo de información.
	</p>

	<h2>4. Derechos de las personas usuarias</h2>
	<p>
		Toda persona puede solicitar información sobre sus datos y pedir su rectificación, actualización o
		supresión cuando resulten inexactos, incompletos, excesivos o se utilicen para fines no autorizados.
		La plataforma ofrece mecanismos de edición de perfil y, cuando sea legalmente posible, eliminación de
		cuenta o datos asociados. Las solicitudes específicas podrán canalizarse por los medios de contacto del
		sitio.
	</p>

	<h2>5. Registro de bases de datos</h2>
	<p>
		Como responsable de bases que contengan datos personales, la plataforma se compromete a cumplir con la
		inscripción en el <strong>Registro Nacional de Bases de Datos</strong> y las exigencias del organismo de
		control, manteniendo la documentación adecuada para eventuales controles.
	</p>

