		Conectando Corazones adopta la licencia <strong>GNU AGPL v3.0</strong>, orientada a la transparencia del
		código y al copyleft incluso en escenarios de software ofrecido como servicio en red. El uso, modificación
		y distribución del software están sujetos a las condiciones de esa licencia. En el marco argentino, su
		compatibilidad con la Ley N.º 11.723 supone respeto a los derechos morales del autor y cumplimiento de
		las condiciones de la licencia.
	</p>
	<ul>
		<li>Refuerza el carácter social y comunitario del proyecto y reduce el riesgo de apropiación opaca.</li>
		<li>
			Puede facilitar auditorías y contribuciones externas. Se prevé publicación del código en repositorio
			público, archivo de autoría y dependencias, y registro formal ante la DNDA cuando corresponda.
		</li>
		<li>
			Transferencias a otras organizaciones requerirán instrumentos jurídicos adecuados y, si aplica,
			registro ante la DNDA.
		</li>
	</ul>

	<h2>3. Modelo económico</h2>
	<p>
		El proyecto se concibe como iniciativa <strong>sin fines de lucro</strong>. No se prevé monetización
		directa de las funcionalidades centrales: no se cobran comisiones por donaciones en la plataforma, ni
		planes de suscripción ni servicios premium orientados a lucrar con la ayuda solidaria. La sostenibilidad
		pretende apoyarse en alianzas y gestión responsable de recursos. Esta limitación de ingresos regulares
		puede afectar la velocidad de evolución del sistema; se compensará con planificación y alianzas
		institucionales.
	</p>

