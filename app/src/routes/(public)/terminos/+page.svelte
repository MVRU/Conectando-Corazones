	<h1>Términos y condiciones de uso</h1>
	<p class="lead text-slate-600">
		<strong>Versión 1.0.</strong> Al registrarte y utilizar Conectando Corazones aceptás estos términos en la
		versión vigente al momento del registro. La plataforma podrá actualizarlos; te notificaremos cambios
		sustanciales cuando corresponda.
	</p>

	<h2>1. Naturaleza del servicio</h2>
	<p>
		Conectando Corazones es una plataforma digital que facilita el vínculo entre instituciones y
		colaboradores para proyectos solidarios y donaciones. La plataforma <strong>no sustituye asesoramiento
		legal, contable ni fiscal</strong>. La información sobre normativa que figura en este documento y en la
		política de privacidad tiene carácter <strong>general e informativo</strong>; cada usuario debe
		consultar a profesionales idóneos según su situación.
	</p>

	<h2>2. Marco legal de referencia del proyecto</h2>
	<p>
		El diseño y la operación de la plataforma consideran, entre otras, las siguientes normas argentinas y
		el régimen de licencia del software. Su mención aquí no implica que la plataforma certifique el
		cumplimiento impositivo o registral de cada usuario o institución.
	</p>

	<h3>2.1 Ley N.º 20.628 — Impuesto a las Ganancias</h3>
	<p>
		Resulta relevante para el tratamiento fiscal de las ganancias y para entender bajo qué condiciones
		pueden ser deducibles ciertas donaciones para quienes tributan dicho impuesto. En particular:
	</p>
	<ul>
		<li>
			Las instituciones que cumplen requisitos de exención (entre otros, que sus ganancias y patrimonio se
			destinen a sus fines institucionales y no se distribuyan) tienen implicancias en su estatus legal y
			fiscal, y pueden condicionar la deducibilidad de las donaciones para los colaboradores cuando la
			entidad esté habilitada conforme a la normativa aplicable (por ejemplo, entidades alcanzadas por
			ARCA en las categorías previstas para donaciones deducibles).
		</li>
		<li>
			Las donaciones a entidades encuadradas según el art. 26 pueden ser deducibles del donante con un
			límite del 5% de la ganancia neta sujeta a impuesto. La plataforma podrá brindar
			<strong>mensajes informativos</strong> sobre esta posibilidad, siempre sujeto a que la institución
			receptora esté debidamente registrada y habilitada y a la correcta documentación de la donación. Se
			facilitará, en la medida de lo posible, la emisión o registro de comprobantes según las
			funcionalidades disponibles.
		</li>
		<li>
			Sobre residencia fiscal: quienes no tengan la condición de residentes en los términos de la ley
			pueden colaborar, pero las donaciones podrían no encuadrarse como deducibles en los mismos términos.
		</li>
		<li>
			Cuando una empresa realice una donación a una institución registrada y habilitada según corresponda,
			la plataforma podrá mostrar un <strong>recordatorio informativo</strong> de que la donación podría
			ser deducible del Impuesto a las Ganancias conforme al art. 81 de la Ley N.º 20.628, sin constituir
			certificación ni asesoramiento.
		</li>
	</ul>

	<h3>2.2 Ley N.º 26.994 — Código Civil y Comercial de la Nación (donación)</h3>
	<p>
		La donación es un <strong>contrato</strong> con efectos jurídicos propios. Conectando Corazones
		<strong>facilita la conexión y el seguimiento</strong>, pero el acto jurídico definitivo puede requerir
		formalidades fuera de la plataforma.
	</p>
	<ul>
		<li>
			<strong>Donaciones simples</strong> (bienes muebles no registrables: ropa, alimentos, útiles,
			electrodomésticos, etc.): en general no exigen escritura pública; pueden perfeccionarse con la
			entrega. La plataforma puede coordinar la relación y ofrecer trazabilidad (por ejemplo, evidencia
			fotográfica o registros internos).
		</li>
		<li>
			<strong>Donaciones formales o solemnes</strong> (inmuebles, vehículos, equipamiento registrable,
			etc.): pueden requerir escritura pública y actuación de escribano y organismos registrales. La
			plataforma puede <strong>intermediar el contacto</strong>, pero el cierre del acto debe completarse
			<strong>fuera</strong> de la plataforma, con asistencia profesional correspondiente.
		</li>
		<li>
			<strong>Aceptación, revocación y cargos:</strong> la aceptación debe producirse en vida del donante
			donde corresponda. Pueden existir causas de revocación (por ejemplo, ingratitud o incumplimiento de
			cargos). La plataforma permite que los colaboradores indiquen el destino deseado de los aportes
			(“cargos”), lo que refuerza la importancia de la transparencia y la rendición de cuentas por parte de
			la institución. La plataforma no gestiona procesos judiciales de revocación.
		</li>
		<li>
			Las reglas sobre validez de la donación y deducibilidad fiscal dependen del cumplimiento de las normas
			generales (objeto, capacidad, forma, estatus de la entidad), <strong>no de la “interprovincialidad”</strong>
			del acto entre jurisdicciones argentinas.
		</li>
	</ul>
	<p>
		<strong>Aplicación en la plataforma:</strong> gestión de cargos por parte del colaborador; evidencia
		documental al cierre del proyecto (imágenes, comprobantes, facturas, descripciones); sistema de reportes
		si se considera incumplido el destino acordado. Estas herramientas buscan la transparencia y no
		reemplazan la vía judicial.
	</p>

	<h3>2.3 Ley N.º 26.388 — Delitos informáticos</h3>
	<p>
		La normativa sanciona accesos indebidos, manipulación o robo de datos, suplantación y fraudes
		electrónicos. La plataforma implementa medidas razonables de seguridad, incluyendo uso de HTTPS,
		prácticas de ciberseguridad en la infraestructura (por ejemplo, las provistas por el proveedor técnico),
		registro de actividad con marcas de tiempo cuando corresponda, y canales para reportar irregularidades.
		Los proyectos con comportamiento irregular pueden ser marcados para revisión o auditoría según las
		políticas internas de la plataforma.
	</p>

	<h3>2.4 Ley N.º 11.723 — Propiedad intelectual</h3>
	<p>
		El código fuente y los elementos creativos del sistema pueden estar amparados como obra en los términos
		de la ley, en tanto expresión original. La ley protege la forma concreta de la obra, no las ideas o
		funcionalidades abstractas. Las transferencias de titularidad, cuando existan, deberán ajustarse a la
		normativa y, si corresponde, a registros ante la autoridad competente.
	</p>

